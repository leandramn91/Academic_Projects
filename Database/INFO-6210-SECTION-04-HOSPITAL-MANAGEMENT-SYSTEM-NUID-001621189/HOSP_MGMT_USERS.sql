-- DROP USERS STATEMENTS
DROP USER ADMIN;
DROP USER DEVELOPER;
DROP USER TESTER;
DROP USER END_USER;

-- GRANT STATEMENTS
GRANT ALL ON hospital_mgmt_sys.* TO ADMIN;
